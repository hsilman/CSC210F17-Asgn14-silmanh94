public class Tower {
    // initialize variables
    int size;
    int total;

    // constructor
    public Tower(int size, int total) {
        super();
        this.size = size;
        this.total = total;
    }

    // size getter and setters
    public int getSize() {
        return size;
    }

    public void setSize() {
        this.size = size;
    }

}
